    private void populateDeleteStoreTable() {

        DefaultTableModel model = (DefaultTableModel) tblDeleteStore.getModel();

        model.setRowCount(0);
        for (Person user : system.getUserAccountDirectory().getUserAccountList()) {

            if ("models.Role.StoreRole".equals(user.getRole().getClass().getName())) {
                Object[] row = new Object[3];
                
                row[0] = user.getName();
                row[1] = user.getUsername();
                row[2] = user.getPassword();
                model.addRow(row);
            } 
        }

    }
